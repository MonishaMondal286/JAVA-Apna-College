public class switchinjava {
    public static void main(String args[]){
        int number = 1;
        switch(number){
            case 1 : System.out.println("pizza");
            break;
            case 2 : System.out.println("burger");
            break;
            case 3 : System.out.println("money");
            break;
            default : System.out.println("save money");
        }
    }
}
